async function run() {
  const allServices = await db.select().from(services);
  const nameBySlug = new Map(
    allServices.filter((s) => s.slug).map((s) => [s.slug as string, s.name]),
  );
  for (const dir of CONTENT) {
    const [updatedDir] = await db
      .update(directions)
      .set({
        accent: dir.accent,
        heroTitle: dir.heroTitle,
        intro: dir.intro,
        heroImageUrl: dir.heroImageUrl,
        body: dir.body,
        metaTitle: dir.metaTitle,
        metaDescription: dir.metaDescription,
      })
      .where(eq(directions.slug, dir.slug))
      .returning();
    if (!updatedDir) {
      console.warn(`⚠ Направление не найдено: ${dir.slug}`);
      continue;
    }
    for (const svc of dir.services) {
      const svcName = nameBySlug.get(svc.slug) ?? "";
      const [u] = await db
        .update(services)
        .set({
          shortDescription: svc.shortDescription,
          description: svc.description,
          price: svc.price,
          duration: svc.duration,
          metaTitle: svcName ? `${svcName} — ${dir.heroTitle}` : "",
          metaDescription: svc.shortDescription,
        })
        .where(eq(services.slug, svc.slug))
        .returning();
      if (!u) console.warn(`  ⚠ Услуга не найдена: ${svc.slug}`);
    }
    console.log(`✓ ${dir.slug}: направление + ${dir.services.length} услуг`);
  }
  console.log("Готово.");
  process.exit(0);
}

run().catch((e) => {
  console.error(e);
  process.exit(1);
});
